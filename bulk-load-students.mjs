function parseStudents(rawData) {
  const lines = rawData.split('\n');
  const students = [];
  let currentGrade = null;
  let currentGroup = null;
  let currentSubject = null;
  let currentTeacher = null;

  for (const line of lines) {
    const trimmed = line.trim();
    if (trimmed.startsWith('GRADE:')) {
      currentGrade = trimmed.split(':')[1].trim();
    } else if (trimmed.startsWith('GROUP:')) {
      currentGroup = trimmed.split(':')[1].trim();
    } else if (trimmed.includes('BUSINESS STUDIES') || trimmed.includes('PHYSICAL SCIENCES')) {
      currentSubject = trimmed.split(' ')[0] + ' ' + trimmed.split(' ')[1];
    } else if (trimmed.startsWith('MS ') || trimmed.startsWith('MR ') || trimmed.startsWith('MISS ')) {
      currentTeacher = trimmed;
    } else if (trimmed.match(/^\d+\s+\d+\s+[A-Z]/)) {
      // Parse student line
      const parts = trimmed.split(/\s+/);
      if (parts.length >= 6) {
        const no = parts[0];
        const admnr = parts[1];
        const surname = parts[2].replace(',', '');
        const initials = parts[3];
        const preferred = parts[4].replace(/[()]/g, '');
        const gender = parts[5];
        const classGroup = parts[6];
        const luritsNumber = parts[7];
        const familyCode = parts[8];

        students.push({
          no,
          admnr,
          surname,
          initials,
          preferred,
          gender,
          classGroup,
          luritsNumber,
          familyCode,
          grade: currentGrade,
          group: currentGroup,
          subject: currentSubject,
          teacher: currentTeacher
        });
      }
    }
  }

  return students;
}

async function bulkLoadStudents() {
  const students = parseStudents(rawData);
  console.log(`Parsed ${students.length} students`);

  // Get all existing users
  const { data: usersData } = await supabase.auth.admin.listUsers();
  const userMap = new Map(usersData.users.map(u => [u.email, u.id]));

  // Group by grade and subject
  const grouped = {};
  for (const student of students) {
    const key = `${student.grade}-${student.subject}`;
    if (!grouped[key]) grouped[key] = [];
    grouped[key].push(student);
  }

  console.log('Groups:', Object.keys(grouped));

  // For each group, create subject class if needed, then students
  for (const [key, groupStudents] of Object.entries(grouped)) {
    const [grade, subject] = key.split('-');
    const gradeLevel = parseInt(grade);

    // Find or create grade
    let gradeRecord = await supabase.from('grades').select('*').eq('level', gradeLevel).single();
    if (!gradeRecord.data) {
      const { data, error } = await supabase.from('grades').insert({ name: `Grade ${grade}`, level: gradeLevel }).select().single();
      if (error) throw error;
      gradeRecord = { data };
    }

    // Find or create subject
    let subjectRecord = await supabase.from('subjects').select('*').eq('name', `${subject} - Grade ${grade}`).eq('grade_tier', grade).single();
    if (!subjectRecord.data) {
      const { data, error } = await supabase.from('subjects').insert({
        name: `${subject} - Grade ${grade}`,
        description: `${subject} curriculum for Grade ${grade}`,
        grade_tier: grade,
        category: 'core',
        access_type: 'Free',
        thumbnail: ''
      }).select().single();
      if (error) throw error;
      subjectRecord = { data };
    }

    // Create subject class
    const className = `${grade} ${groupStudents[0].group}`;
    let subjectClass = await supabase.from('subject_classes').select('*').eq('subject_id', subjectRecord.data.id).eq('name', className).single();
    if (!subjectClass.data) {
      const { data, error } = await supabase.from('subject_classes').insert({
        subject_id: subjectRecord.data.id,
        name: className,
        teacher_id: null, // TODO: create teachers
        capacity: 40,
        grade_id: gradeRecord.data.id
      }).select().single();
      if (error) throw error;
      subjectClass = { data };
    }

    // Create register class
    const registerClassName = `${grade} ${groupStudents[0].classGroup}`;
    let registerClass = await supabase.from('register_classes').select('*').eq('grade_id', gradeRecord.data.id).eq('name', registerClassName).single();
    if (!registerClass.data) {
      const { data, error } = await supabase.from('register_classes').insert({
        name: registerClassName,
        grade_id: gradeRecord.data.id,
        class_teacher_id: null,
        max_students: 40
      }).select().single();
      if (error) throw error;
      registerClass = { data };
    }

    // Now create students
    for (const student of groupStudents) {
      // Create auth user or get existing
      const email = `${student.admnr}@glenbrack.edu`;
      const password = 'TempPass123!'; // TODO: generate proper pins

      let userId;
      if (userMap.has(email)) {
        userId = userMap.get(email);
      } else {
        const { data: authUser, error: authError } = await supabase.auth.admin.createUser({
          email,
          password,
          user_metadata: { full_name: `${student.surname} ${student.preferred}` }
        });
        if (authError) {
          console.error('Auth error for', student.admnr, authError);
          continue;
        }
        userId = authUser.user.id;
        userMap.set(email, userId);
      }

      // Check if profile exists
      const { data: existingProfile } = await supabase.from('profiles').select('id').eq('id', userId).maybeSingle();
      if (!existingProfile) {
        // Create profile
        const { error: profileError } = await supabase.from('profiles').insert({
          id: userId,
          full_name: `${student.surname} ${student.preferred}`,
          email,
          role: 'student',
          pin: password
        });
        if (profileError) {
          console.error('Profile error for', student.admnr, profileError);
          continue;
        }
      }

      // Check if student record exists
      const { data: existingStudent } = await supabase.from('students').select('id').eq('id', userId).maybeSingle();
      if (!existingStudent) {
        // Create student record
        const { error: studentError } = await supabase.from('students').insert({
          id: userId,
          first_name: student.preferred,
          last_name: student.surname,
          administration_number: student.admnr,
          gender: student.gender,
          admission_year: '2026',
          grade_id: gradeRecord.data.id,
          register_class_id: registerClass.data.id,
          status: 'active',
          pin: password
        });
        if (studentError) {
          console.error('Student error for', student.admnr, studentError);
          continue;
        }
      }

      // Check if subject assignment exists
      const { data: existingAssignment } = await supabase.from('student_subjects').select('id').eq('student_id', userId).eq('subject_id', subjectRecord.data.id).maybeSingle();
      if (!existingAssignment) {
        // Assign subject
        const { error: assignError } = await supabase.from('student_subjects').insert({
          student_id: userId,
          subject_id: subjectRecord.data.id
        });
        if (assignError) {
          console.error('Assignment error for', student.admnr, assignError);
          continue;
        }
      }

      console.log(`Processed student: ${student.admnr} ${student.surname}`);
    }
  }

  console.log('Bulk load completed');
}

